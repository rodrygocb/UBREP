        require_once('db_connection.php');

        if ($_SERVER["REQUEST_METHOD"] == "GET") {
            $query = isset($_GET["q"]) ? '%' . $_GET["q"] . '%' : '%';
            $author = isset($_GET["author"]) ? '%' . $_GET["author"] . '%' : '%';
            $isbn = isset($_GET["isbn"]) ? '%' . $_GET["isbn"] . '%' : '%';

            $sql = "SELECT * FROM livros WHERE titulo LIKE ? AND autor LIKE ? AND isbn LIKE ?";

            $stmt = $connection->prepare($sql);
            $stmt->bind_param("sss", $query, $author, $isbn);
            $stmt->execute();
            $result = $stmt->get_result();

            if ($result->num_rows > 0) {
                while ($row = $result->fetch_assoc()) {
                    echo '<div class="col-lg-4 text-center">';
                    echo '<div class="bd-placeholder-img rounded-3 mb-4" style="width: 100%; height: 150px; background-color: var(--bs-secondary-color);"></div>';
                    echo '<h2 class="fw-normal">' . htmlspecialchars($row["titulo"]) . '</h2>';
                    echo '<p>Autor: ' . htmlspecialchars($row["autor"]) . '</p>';
                    echo '<p>ISBN: ' . htmlspecialchars($row["isbn"]) . '</p>';
                    echo '<p><a class="btn btn-secondary" href="detalhes_livro.php?isbn=' . htmlspecialchars($row["isbn"]) . '">Ver detalhes &raquo;</a></p>';
                    echo '</div>';
                }
            } else {
                echo "<p class='col-lg-12 text-center'>Nenhum livro encontrado.</p>";
            }

            $stmt->close();
        }

        $connection->close();
